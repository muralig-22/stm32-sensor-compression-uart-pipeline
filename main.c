static uint32_t packet_counter = 0;

uint8_t delta_encode_24_to_8(int32_t current)
{
    static int32_t prev24 = 0;

    int32_t scaled = current / 10000;
    int32_t delta = scaled - prev24;
    prev24 = scaled;

    if (delta > 127) delta = 127;
    if (delta < -128) delta = -128;

    return (uint8_t)((int8_t)delta);
}

uint8_t delta_encode_16_to_8(int16_t current)
{
    static int16_t prev16 = 0;

    int16_t scaled = current / 100;
    int16_t delta = scaled - prev16;
    prev16 = scaled;
}
int32_t simulate_24bit_sensor(void)
{
    static float t = 0.0f;
    t += 0.1f;

    return (int32_t)(4000000 * sinf(2 * PI * 0.5f * t));
}

int16_t simulate_16bit_sensor(void)
{
    static float t = 0.0f;
    t += 0.1f;

    return (int16_t)(16000 * cosf(2 * PI * 0.5f * t));
}

void create_packet(uint8_t *packet)
{
    uint8_t index = 0;

    packet[index++] = START_BYTE;
    packet[index++] = (uint8_t)(packet_counter & 0xFF);

    for (uint8_t i = 0; i < SAMPLES_PER_PACKET; i++)
    {
        int32_t sensor24 = simulate_24bit_sensor();
        int16_t sensor16 = simulate_16bit_sensor();

        uint8_t d24 = delta_encode_24_to_8(sensor24);
        uint8_t d16 = delta_encode_16_to_8(sensor16);

        packet[index++] = d24;
        packet[index++] = d16;
    }

    packet[index++] = END_BYTE;
}

void send_packet_usart(uint8_t *packet)
{
    char msg[200];
    int len = 0;

    len += sprintf(msg + len, "Packet %lu: ", packet_counter);

    for (int i = 0; i < PACKET_SIZE; i++)
    {
        len += sprintf(msg + len, "%02X ", packet[i]);
    }

    len += sprintf(msg + len, "\r\n");

    HAL_USART_Transmit(&husart2,
                      (uint8_t*)msg,
                      len,
                      HAL_MAX_DELAY);
}

void run_sensor_pipeline(void)
{
    uint8_t packet[PACKET_SIZE];

    while (1)
    {
        create_packet(packet);
        send_packet_usart(packet);
        packet_counter++;

        HAL_Delay(100); // 10 packets/sec
    }
}


